foreach ($new_destinations as $dest) {
    $existing = get_term_by('slug', $dest['slug'], 'destination');
    if ($existing) {
        WP_CLI::log("  [skip] '{$dest['name']}' 이미 존재 (ID: {$existing->term_id})");
        continue;
    }

    $parent_id = 0;
    if (!empty($dest['parent'])) {
        $parent_term = get_term_by('slug', $dest['parent'], 'destination');
        if ($parent_term) {
            $parent_id = $parent_term->term_id;
        } else {
            WP_CLI::warning("부모 텀 '{$dest['parent']}'을(를) 찾을 수 없습니다. 최상위로 등록합니다.");
        }
    }

    $result = wp_insert_term($dest['name'], 'destination', [
        'slug'   => $dest['slug'],
        'parent' => $parent_id,
    ]);

    if (is_wp_error($result)) {
        WP_CLI::warning("텀 생성 실패: {$dest['name']} - " . $result->get_error_message());
    } else {
        WP_CLI::log("  [+] 여행지 등록: {$dest['name']} (ID: {$result['term_id']})");

