// 포스트 생성
// ─────────────────────────────────────────────
foreach ($itineraries as $data) {
    $existing = get_page_by_title($data['title'], OBJECT, 'travel_itinerary');
    if ($existing) {
        WP_CLI::log("  [skip] 이미 존재: {$data['title']}");
        continue;
    }

    $post_id = wp_insert_post([
        'post_type'    => 'travel_itinerary',
        'post_title'   => $data['title'],
        'post_excerpt' => $data['excerpt'],
        'post_content' => $data['content'],
        'post_status'  => 'publish',
        'post_author'  => 1,
    ]);

    if (is_wp_error($post_id)) {
        WP_CLI::warning("포스트 생성 실패: {$data['title']}");
        continue;
    }

    // 택소노미
    $dest_ids = array_filter(array_map(fn($slug) => get_term_id_by_slug($slug, 'destination'), $data['dest_slugs']));
    if ($dest_ids) wp_set_object_terms($post_id, $dest_ids, 'destination');

    $style_ids = [];
    foreach ($data['styles'] as $style_name) {
        $term = get_term_by('name', $style_name, 'travel_style');
        if (!$term) {
            $new = wp_insert_term($style_name, 'travel_style');
            if (!is_wp_error($new)) {
                $style_ids[] = (int)$new['term_id'];
                if (function_exists('pll_set_term_language')) {
                    pll_set_term_language($new['term_id'], 'ko');
                }
            }
        } else {
            $style_ids[] = (int)$term->term_id;
        }
    }
    if ($style_ids) wp_set_object_terms($post_id, $style_ids, 'travel_style');

    // 메타
    foreach ($data['meta'] as $key => $val) {
        update_post_meta($post_id, $key, $val);
    }

    // Days
    if (!empty($data['days'])) {
        update_post_meta($post_id, '_ft_days', $data['days']);
    }

    // Polylang 한국어 설정
    if (function_exists('pll_set_post_language')) {
        pll_set_post_language($post_id, 'ko');
    }

    WP_CLI::log("  [+] #{$post_id} {$data['title']}");
}

