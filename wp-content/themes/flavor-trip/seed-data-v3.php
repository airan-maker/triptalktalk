// 포스트 생성
// ─────────────────────────────────────────────
foreach ($itineraries as $data) {
    $post_id = wp_insert_post([
        'post_type'    => 'travel_itinerary',
        'post_title'   => $data['title'],
        'post_excerpt' => $data['excerpt'],
        'post_content' => $data['content'],
        'post_status'  => 'publish',
    ]);

    if (is_wp_error($post_id)) {
        WP_CLI::warning("포스트 생성 실패: {$data['title']}");
        continue;
    }

    // 택소노미
    $dest_ids = array_filter(array_map(fn($slug) => get_term_id_by_slug($slug, 'destination'), $data['dest_slugs']));
    if ($dest_ids) wp_set_object_terms($post_id, $dest_ids, 'destination');

    $style_ids = array_filter(array_map(fn($name) => get_term_id_by_slug(sanitize_title($name), 'travel_style'), $data['styles']));
    if ($style_ids) wp_set_object_terms($post_id, $style_ids, 'travel_style');

    // 메타
    foreach ($data['meta'] as $key => $val) {
        update_post_meta($post_id, $key, $val);
    }

    // Days
    if (!empty($data['days'])) {
        update_post_meta($post_id, '_ft_days', $data['days']);
    }

    WP_CLI::log("  [+] #{$post_id} {$data['title']}");
}

